statuses = {"выполнено": "1", "в процессе": "2", "отложено": "3"}  # Статусы заметок и их числовые значения
status = "в процессе"  # Текущий статус заметки

while True:  # Бесконечный цикл для работы с программой
    print(f"\nТекущий статус: {status}")  # Вывод текущего статуса
    print("Доступные варианты статуса:")  # Вывод списка возможных статусов
    for key, value in statuses.items():  # Перебор всех статусов
        print(f"{value}: {key}")  # Вывод числового значения и названия статуса

    new_status = input("Введите число, соответствующее новому статусу: ")  # Ввод нового статуса
    for key, value in statuses.items():  # Поиск статуса по введенному числу
        if new_status == value:  # Если найдено соответствие
            status = key  # Обновление текущего статуса
            print(f"Статус успешно обновлен на '{status}'")  # Подтверждение обновления
            break
    else:  # Если введено некорректное значение
        print("Ошибка: Введен некорректный выбор. Попробуйте снова.")  # Сообщение об ошибке